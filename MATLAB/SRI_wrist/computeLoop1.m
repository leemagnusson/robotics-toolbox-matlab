function P = computeLoop1(R01,a,b,c,d,e,f,g)

    p1  =     [ c;0.5*6.6*cos(pi/4);b];
    p2  =     [-c;0.5*6.6*cos(pi/4);b];
    p3  = R01*[-2.5;0.5*6.6*cos(pi/4);g];
    p4  = R01*[ 2.5;0.5*6.6*cos(pi/4);g];
    p5  = R01*[ 0.5*d;0.5*6.6*cos(pi/4);e];
    p6  = R01*[ 0.5*a*cos(-0.3142*0);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*0)];
    p7  = R01*[ 0.5*a*cos(-0.3142*1);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*1)];
    p8  = R01*[ 0.5*a*cos(-0.3142*2);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*2)];
    p9  = R01*[ 0.5*a*cos(-0.3142*3);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*3)];
    p10 = R01*[ 0.5*a*cos(-0.3142*4);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*4)];
    p11 = R01*[ 0.5*a*cos(-0.3142*5);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*5)];
    p12 = R01*[ 0.5*a*cos(-0.3142*6);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*6)];
    p13 = R01*[ 0.5*a*cos(-0.3142*7);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*7)];
    p14 = R01*[ 0.5*a*cos(-0.3142*8);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*8)];
    p15 = R01*[ 0.5*a*cos(-0.3142*9);0.5*6.6*cos(pi/4);-f+0.5*a*sin(-0.3142*9)];
    p16 = R01*[ 0.5*a*cos(-0.3142*10);0.5*6.6*cos(pi/4);-f-0.5*a*sin(0.3142*10)];
    p17 = R01*[-0.5*d;0.5*6.6*cos(pi/4);e];
    P = [p1,p2,p3,p4,p5,p6,p7,p8,p9,p10,p11,p12,p13,p14,p15,p16,p17];
    plot3(P(1,:),P(2,:),P(3,:),'m*');

end